from pmath import factorial, fibonacci, pi, e, sin, cos, combinations


if __name__ == '__main__':
	print(f"fact(10) = {factorial(10)}")
	print(f"fib(10) = {fibonacci(10)}")
	print(f"pi = {pi()}")
	print(f"e = {e()}")
	print(f"sin(30) = {sin(30)}")
	print(f"cos(45) = {cos(45)}")
	print(f"combinations(10, 1) = {combinations(10, 1)}")
